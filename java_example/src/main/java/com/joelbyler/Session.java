package com.joelbyler;

public class Session {
	private String title;
	private String abstr;
	private String difficulty;
	private String speakerName;
	private String technology;
	private String uri;
	private String eventType;
	private String id;
	private String sessionLookupId;
	private String speakerUri;

	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}

	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}

	/**
	 * @return the abstr
	 */
	public String getAbstr() {
		return abstr;
	}

	/**
	 * @param abstr the abstr to set
	 */
	public void setAbstr(String abstr) {
		this.abstr = abstr;
	}

	/**
	 * @return the difficulty
	 */
	public String getDifficulty() {
		return difficulty;
	}

	/**
	 * @param difficulty the difficulty to set
	 */
	public void setDifficulty(String difficulty) {
		this.difficulty = difficulty;
	}

	/**
	 * @return the speakerName
	 */
	public String getSpeakerName() {
		return speakerName;
	}

	/**
	 * @param speakerName the speakerName to set
	 */
	public void setSpeakerName(String speakerName) {
		this.speakerName = speakerName;
	}

	/**
	 * @return the technology
	 */
	public String getTechnology() {
		return technology;
	}

	/**
	 * @param technology the technology to set
	 */
	public void setTechnology(String technology) {
		this.technology = technology;
	}

	/**
	 * @return the uri
	 */
	public String getUri() {
		return uri;
	}

	/**
	 * @param uri the uri to set
	 */
	public void setUri(String uri) {
		this.uri = uri;
	}

	/**
	 * @return the eventType
	 */
	public String getEventType() {
		return eventType;
	}

	/**
	 * @param eventType the eventType to set
	 */
	public void setEventType(String eventType) {
		this.eventType = eventType;
	}

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the sessionLookupId
	 */
	public String getSessionLookupId() {
		return sessionLookupId;
	}

	/**
	 * @param sessionLookupId the sessionLookupId to set
	 */
	public void setSessionLookupId(String sessionLookupId) {
		this.sessionLookupId = sessionLookupId;
	}

	/**
	 * @return the speakerUri
	 */
	public String getSpeakerUri() {
		return speakerUri;
	}

	/**
	 * @param speakerUri the speakerUri to set
	 */
	public void setSpeakerUri(String speakerUri) {
		this.speakerUri = speakerUri;
	}

}

